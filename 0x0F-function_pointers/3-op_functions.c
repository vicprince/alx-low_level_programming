/**
 * op_add - add @a and @b
 * @a: integer
 * @b: integer
 *
 * Return: return Success
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract @b from @a
 * @a: integer
 * @b: integer
 *
 * Return: Success
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies @a by @b
 * @a: integer
 * @b: integer
 *
 * Return: Success
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides @a by @b
 * @a: integer
 * @b: integer
 *
 * Return: Success
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of @a by @b
 * @a: integer
 * @b: integer
 *
 * Return: Success
 */
int op_mod(int a, int b)
{
	return (a % b);
}
